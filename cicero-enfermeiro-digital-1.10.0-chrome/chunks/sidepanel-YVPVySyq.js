/** Resolve the vendor-prefixed SpeechRecognition constructor, if any. */
function getSpeechRecognitionCtor() {
	if (typeof window === "undefined") return void 0;
	return window.webkitSpeechRecognition || window.SpeechRecognition;
}
/** Map a raw SpeechRecognition error code to a friendly pt-BR message. */
function friendlyError(code) {
	if (code === "not-allowed" || code === "service-not-allowed") return "Preciso de permissão para usar o microfone.";
	if (code === "no-speech") return "Não ouvi nada. Pode falar de novo?";
	return "Tive um problema com o reconhecimento de voz. Pode repetir?";
}
/**
* Zero-latency, key-less speech-to-text via the browser's
* `webkitSpeechRecognition`.
*
* Ideal as a fast path, but can be flaky inside an extension side panel — so
* callers should fall back to {@link GeminiAudioTranscriber} when
* {@link supported} is false or recognition errors out.
*/
var WebSpeechTranscriber = class {
	supported;
	/** Streams the running transcript into the UI while the user is talking. */
	onResult = null;
	#lang;
	#recognition = null;
	#transcript = "";
	#error = null;
	#ended = false;
	#startResolve = null;
	#startReject = null;
	#stopResolve = null;
	#stopReject = null;
	constructor(config) {
		this.#lang = config.lang || "pt-BR";
		this.supported = !!getSpeechRecognitionCtor();
	}
	start() {
		if (!this.supported) return Promise.reject(/* @__PURE__ */ new Error("O reconhecimento de voz não está disponível neste navegador."));
		return new Promise((resolve, reject) => {
			const recognition = new (getSpeechRecognitionCtor())();
			recognition.lang = this.#lang;
			recognition.interimResults = true;
			recognition.continuous = true;
			this.#transcript = "";
			this.#error = null;
			this.#ended = false;
			this.#startResolve = resolve;
			this.#startReject = reject;
			recognition.onstart = () => {
				this.#settleStart();
			};
			recognition.onresult = (event) => {
				let text = "";
				for (let i = 0; i < event.results.length; i++) text += event.results[i][0].transcript;
				this.#transcript = text.trim();
				this.onResult?.(this.#transcript, false);
			};
			recognition.onerror = (event) => {
				this.#error = new Error(friendlyError(event?.error));
			};
			recognition.onend = () => {
				this.#ended = true;
				this.#failStart(this.#error ?? /* @__PURE__ */ new Error("O reconhecimento de voz não iniciou."));
				this.#settleStop();
			};
			this.#recognition = recognition;
			try {
				recognition.start();
			} catch {
				this.#startResolve = null;
				this.#startReject = null;
				reject(/* @__PURE__ */ new Error("Não consegui iniciar o reconhecimento de voz."));
			}
		});
	}
	stop() {
		if (!this.#recognition || this.#ended) {
			if (this.#error) return Promise.reject(this.#error);
			return Promise.resolve(this.#transcript);
		}
		return new Promise((resolve, reject) => {
			this.#stopResolve = resolve;
			this.#stopReject = reject;
			try {
				this.#recognition.stop();
			} catch {
				this.#settleStop();
			}
		});
	}
	abort() {
		this.#startResolve = null;
		this.#startReject = null;
		this.#stopResolve = null;
		this.#stopReject = null;
		this.#transcript = "";
		this.#ended = true;
		const recognition = this.#recognition;
		this.#recognition = null;
		if (recognition) try {
			recognition.abort();
		} catch {}
	}
	#settleStart() {
		const resolve = this.#startResolve;
		this.#startResolve = null;
		this.#startReject = null;
		resolve?.();
	}
	#failStart(error) {
		const reject = this.#startReject;
		this.#startResolve = null;
		this.#startReject = null;
		reject?.(error);
	}
	#settleStop() {
		if (this.#error) {
			const reject = this.#stopReject;
			this.#stopResolve = null;
			this.#stopReject = null;
			reject?.(this.#error);
			return;
		}
		const resolve = this.#stopResolve;
		this.#stopResolve = null;
		this.#stopReject = null;
		resolve?.(this.#transcript);
	}
};
